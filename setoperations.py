set1 = {0,2,4,6,8}
set2 = {1,2,3,4,5}
# union of set1 and set2
a= set1.union(set2)
print(a)
# intersection of set1 and set2
b= set1.intersection(set2)
print(b)
# difference of set1 and set2
c = set1.difference(set2)
print(c)
# symmetric difference of set1 and  set2
print(set1.symmetric_difference(set2))
